let numeroUsuario = parseInt(prompt("Ingresa un numero entre 1 y 10"));

if (numeroUsuario <= 10 && numeroUsuario >= 1) {
    console.log("Efectivamente, está en el rango");
} else {
    console.log("No está en el rango de números");
}
